# Passo 1: Solicitar ao usuário as dimensões do retângulo
base = float(input("Digite o valor da base do retângulo: "))
altura = float(input("Digite o valor da altura do retângulo: "))

# Passo 2: Calcular a área do retângulo (Área = base * altura)
area_retangulo = base * altura

# Passo 3: Exibir o resultado
print(f"A área do retângulo com base {base} e altura {altura} é: {area_retangulo}")
